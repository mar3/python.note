class logger:

	@staticmethod
	def error(*arguments):

		out.println(util.timestamp(), ' [error] ', *arguments)

	@staticmethod
	def info(*arguments):

		out.println(util.timestamp(), ' [info] ', *arguments)

	@staticmethod
	def debug(*arguments):

		out.println(util.timestamp(), ' [debug] ', *arguments)






